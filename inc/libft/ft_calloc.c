#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	void	*i;

	i = malloc(nmemb * size);
	if (i == NULL)
		return (i);
	ft_memset(i, 0, nmemb * size);
	return (i);
}
