#include "libft.h"

char	*ft_strmapi(const char *s, char (*f)(unsigned int, char))
{
	unsigned int	i;
	char			*j;

	i = 0;
	if (!s || (!s && !f))
		return (ft_strdup(""));
	else if (!f)
		return (ft_strdup(s));
	j = ft_strdup(s);
	if (!j)
		return (j = NULL);
	while (s[i])
	{
		j[i] = (*f)(i, s[i]);
		i++;
	}
	return (j);
}
