#include "../../inc/minishell.h"

void	pwd(int fdout)
{
	char	buf[256];

	getcwd(buf, 256);
	write(fdout, &buf, ft_strlen(buf));
	write(fdout, "\n", 1);
	g_exit_status = 0;
}
